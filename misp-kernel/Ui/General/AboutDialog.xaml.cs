namespace Misp.Kernel.Ui.General
{
    /// <summary>
    /// Interaction logic for AboutDialog.xaml
    /// </summary>
    public partial class AboutDialog : Window
    {
        public AboutDialog()
        {
            InitializeComponent();            
            this.ResizeMode = System.Windows.ResizeMode.NoResize;
            this.Title = "About B-Cephal";
            //this.licence.Content = "Licenced to : Moriset & Co";
            this.version.Content = "B-CEPHAL Version " + Application.ApplicationManager.Instance.ApplicationVersion;// "Version 1.1";
            this.right.Content = "© 2016 Moriset & Co (Belgium)";
            this.rightReserved.Content = "All rights reserved";
            CenterWindowOnScreen();
       
        }

        private void onClick(object sender, RoutedEventArgs e)
        {
            this.Close();
        }

        private void Hyperlink_RequestNavigate(object sender, System.Windows.Navigation.RequestNavigateEventArgs e)
        {
            Process.Start(new ProcessStartInfo(e.Uri.AbsoluteUri));
            e.Handled = true;
        }
        private void CenterWindowOnScreen()
        {
            double screenWidth = System.Windows.SystemParameters.PrimaryScreenWidth;
            double screenHeight = System.Windows.SystemParameters.PrimaryScreenHeight;
            double windowWidth = this.Width;
            double windowHeight = this.Height;
            this.Left = (screenWidth / 2) - (windowWidth / 2);
            this.Top = (screenHeight / 2) - (windowHeight / 2);
        }
    }
}
